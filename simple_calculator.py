# Simple arithmetic calculations.

# Asks 2 values.
value_one = int(input("Enter value 1: "))
value_two = int(input("Enter value 2: "))

# Addition.
print(f"Addition: {value_one + value_two}")
print(f"Subtraction: {value_one - value_two}")
print(f"Multiplication: {value_one * value_two}")
print(f"Division: {value_one / value_two}")
print(f"Remainder (Modulus): {value_one % value_two}")